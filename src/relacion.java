    public void initTabla()
    {
        List<Empleado> lista = null;
        try
        {
            Operacion c = (Operacion)Naming.lookup("rmi://"+direccion+"/ProyectoServidor");
            lista = c.listar();

            DefaultTableModel modelotabla = (DefaultTableModel)jTable1.getModel();
            modelotabla.setRowCount(0);
            Object[] fila = new Object[5];

            for(int i=0; i < lista.size(); i++)
            {
                fila[0] = lista.get(i).getClave();
                fila[1] = lista.get(i).getNombr();
                fila[2] = lista.get(i).getSueld();
                fila[3] = lista.get(i).getDiast();
                fila[4] = lista.get(i).getTotal();
                modelotabla.addRow(fila);
            }
            jTable1.requestFocus();
        }
        catch(MalformedURLException murle)
        {
            System.out.println(); System.out.println("MalformedURLException"); System.out.println(murle);
        }
        catch(RemoteException re)
        {
            System.out.println(); System.out.println("RemoteException"); System.out.println(re);
        }
        catch(NotBoundException nbe) 
        {
            System.out.println(); System.out.println("NotBoundException"); System.out.println(nbe);
        }
        catch(java.lang.ArithmeticException ae) 
        {
            System.out.println(); System.out.println("java.lang.ArithmeticException"); System.out.println(ae);
        }
    }

